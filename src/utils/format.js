// Funcionalidades

function getSubject(subjectNumber) {
    const position = subjects[+subjectNumber - 1]
    return position
}

function covertHoursToMinutes(time) {
    const [hour, minutes] = time.split(":")
    return Number((hour * 60) + minutes)
}

